package day03_datacasting_increment;

public class C3_DataCasting {

	public static void main(String[] args) {
		
		byte sayi1 = 10;
		
		System.out.println(sayi1);
		
		short sayi2 = sayi1;
		
		System.out.println(sayi2);
		
		float sayi3 = sayi2;
		
		System.out.println(sayi3);
		
		double sayi4 = sayi2;
		
		System.out.println(sayi4);

	}

}
